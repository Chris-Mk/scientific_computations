fib1 = 1
fib2 = 2
even_sum = 0

while True:
    fib_next = fib1 + fib2

    if fib_next >= 4000000:
        break

    if fib_next % 2 == 0:
        even_sum += fib_next

    fib1 = fib2
    fib2 = fib_next

print(even_sum)
